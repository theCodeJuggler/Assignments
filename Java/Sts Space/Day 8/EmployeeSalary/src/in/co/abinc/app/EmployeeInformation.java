package in.co.abinc.app;

public class EmployeeInformation {

	//instance members
	private String empID;
	private String employeeName;
	private String employeeDes;
	private double employeeSalary;
	private String employeeComm;
	
	//getter setter methods
	public String getEmpID() {
		return empID;
	}
	
	public void setEmpID(String empID) {
		this.empID = empID;
	}
	
	public String getEmployeeName() {
		return employeeName;
	}
	
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	
	public String getEmployeeDes() {
		return employeeDes;
	}
	
	public void setEmployeeDes(String employeeDes) {
		this.employeeDes = employeeDes;
	}
	
	public double getEmployeeSalary() {
		return employeeSalary;
	}
	
	public void setEmployeeSalary(double employeeSalary) {
		this.employeeSalary = employeeSalary;
	}
	
	public String getEmployeeComm() {
		return employeeComm;
	}
	
	public void setEmployeeComm(String employeeComm) {
		this.employeeComm = employeeComm;
	}
	
}
